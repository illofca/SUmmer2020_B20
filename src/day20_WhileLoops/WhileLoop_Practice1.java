package day20_WhileLoops;

public class WhileLoop_Practice1 {
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i++) {
            System.out.println(i + " ");
        }
        System.out.println();

        int num = 1;
        while (num <= 10){

            System.out.println(num+" ");
            num++; //itirator, not mandatory, but if you give, give at the end
        }
    }
}
