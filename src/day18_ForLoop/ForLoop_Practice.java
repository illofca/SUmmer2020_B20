package day18_ForLoop;

public class ForLoop_Practice {
    public static void main(String[] args) {


        for (int i = 1; i <= 10; i++) {  // i:1, 2, 3, 4, 5
            // pre or post increment does not matter
            System.out.println("Cybertek Batch 20"); //1, 2, 3, 4, 5 ==>
            // ends when sees i = 6 and will not execute the statement


        }
        System.out.println("==================================");

        for (int i = 1; i <= 10; ++i) {
            System.out.print(i + "\n");

        }

        System.out.println(" ");


        System.out.println("Hello");

        System.out.println("=========================");
        for (int i = 100; i >= 1; i--) {
            System.out.print(i + " ");}
        System.out.println();
        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
            }
        }
    }
